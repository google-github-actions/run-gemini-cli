import * as fs from 'node:fs';
import * as path from 'node:path';

interface VitestReport {
  testResults: {
    assertionResults: {
      title: string;
      status: 'passed' | 'failed' | 'skipped';
      failureMessages: string[];
      duration: number;
    }[];
  }[];
}

interface SummaryRow {
  name: string;
  total: number;
  passed: number;
  avgDuration: number;
}

function main() {
  const args = process.argv.slice(2);
  if (args.length === 0) {
    console.error(
      'Usage: npx tsx scripts/aggregate_evals.ts <path-to-vitest-report.json> ...',
    );
    process.exit(1);
  }

  const summaries: SummaryRow[] = [];
  const allFailures: { title: string; message: string; evalName: string }[] =
    [];

  for (const reportPath of args) {
    if (!fs.existsSync(reportPath)) {
      console.warn(`File not found: ${reportPath}`);
      continue;
    }

    try {
      const content = fs.readFileSync(reportPath, 'utf-8');
      if (!content.trim()) {
        console.warn(`Empty file: ${reportPath}`);
        continue;
      }

      const report: VitestReport = JSON.parse(content);
      const evalName = path
        .basename(reportPath, '.json')
        .replace('eval-results-', '')
        .replace('gemini-3-flash-preview-', ''); // Simplified name

      let total = 0;
      let passed = 0;
      let totalDuration = 0;

      for (const testResult of report.testResults) {
        for (const assertion of testResult.assertionResults) {
          total++;
          totalDuration += assertion.duration || 0;
          if (assertion.status === 'passed') {
            passed++;
          } else if (assertion.status === 'failed') {
            allFailures.push({
              title: assertion.title,
              message: assertion.failureMessages.join('\n'),
              evalName,
            });
          }
        }
      }

      summaries.push({
        name: evalName,
        total,
        passed,
        avgDuration: total > 0 ? totalDuration / total / 1000 : 0,
      });
    } catch (e) {
      console.error(`Error processing ${reportPath}:`, e);
    }
  }

  if (summaries.length === 0) {
    console.error('No valid evaluation reports found.');
    process.exit(1);
  }

  // Aggregate results
  const total = summaries.reduce((sum, s) => sum + s.total, 0);
  const passed = summaries.reduce((sum, s) => sum + s.passed, 0);
  const totalDurationSum = summaries.reduce(
    (sum, s) => sum + s.avgDuration * s.total,
    0,
  );
  const avgDuration = total > 0 ? totalDurationSum / total : 0;
  const passRateRaw = total > 0 ? (passed / total) * 100 : 0;

  console.log(`## 📊 Gemini CLI Quality Report`);
  console.log(``);
  console.log(`### 📊 Overall Status`);
  console.log(
    `- **Pass Rate:** ${passRateRaw.toFixed(1)}% (${passed}/${total})`,
  );
  console.log(`- **Avg Latency:** ${avgDuration.toFixed(2)}s`);
  console.log(``);

  console.log(`### 📂 Breakdown by Evaluation`);
  console.log(`| Evaluation | Pass Rate | Passed | Total | Avg Latency |`);
  console.log(`| :--- | :--- | :--- | :--- | :--- |`);
  for (const s of summaries) {
    const rate = s.total > 0 ? (s.passed / s.total) * 100 : 0;
    console.log(
      `| \`${s.name}\` | ${rate.toFixed(1)}% | ${s.passed} | ${s.total} | ${s.avgDuration.toFixed(2)}s |`,
    );
  }
  console.log(``);

  if (allFailures.length > 0) {
    console.log(`### ❌ Failures (${allFailures.length})`);
    for (const failure of allFailures) {
      console.log(`<details>`);
      console.log(
        `<summary><b>[${failure.evalName}] ${failure.title}</b></summary>`,
      );
      console.log(``);
      console.log('```');
      console.log(failure.message);
      console.log('```');
      console.log(`</details>`);
    }
  } else {
    console.log(`### ✅ All functional benchmarks passed!`);
  }

  console.log(`\n---\n*Generated by evaluation framework*`);

  if (passRateRaw < 90) {
    console.error(
      `\n❌ Overall pass rate ${passRateRaw.toFixed(1)}% is below the 90% threshold.`,
    );
    process.exit(1);
  } else {
    console.log(
      `\n✅ Overall pass rate ${passRateRaw.toFixed(1)}% meets the 90% threshold.`,
    );
  }
}

main();
